package com.albertech.inputdemo.chatoperator.func.emoji;


public class EmojiBean {

    private int res;
    private String code;


    public EmojiBean(int res, String code) {
        this.res = res;
        this.code = code;
    }


    public int getRes() {
        return res;
    }

    public String getCode() {
        return code;
    }
}
