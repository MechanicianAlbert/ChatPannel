package com.albertech.inputdemo.chatoperator.func.plus;


public class PlusBean {

    private int res;
    private String name;


    public PlusBean(int res, String name) {
        this.res = res;
        this.name = name;
    }


    public int getRes() {
        return res;
    }

    public String getName() {
        return name;
    }
}
